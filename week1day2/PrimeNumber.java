package week1day2;

public class PrimeNumber {

	public static void main(String[] args) {
		System.out.println("The even number list below:");
		
		for (int i =20 ; i <=40 ; i++) {
			if (i%2 ==0) {
				System.out.println(i+" is an even number");
			}
			else {
				System.out.println(i+" is not an even number");
			}
		}

	}

}
